// 19. Write a Java program to convert a given string into lowercase. Sample
//                     Input: THE QUICK BROWN FOX JUMPS OVER THE LAZY DOG.
//                     Output: the quick brown fox jumps over the lazy dog.
public class Nineteen {
    public static void main(String[] args) {    // Main method
        String name = "THE QUICK BROWN FOX JUMPS OVER THE LAZY DOG";      // instance variable

        System.out.println(name.toLowerCase());        // Called instance in method
    }
}
